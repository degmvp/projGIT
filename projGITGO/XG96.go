// Go program to show the slice 
// - composite literal
package main
  
import "fmt"
  
func main() {
  
    // Slice with composite literal
    // Slice allows you to group together
    // the values of the same type 
    // here type of values is int
    s1 := []int{23, 56, 89, 34}
      
    // displaying the values
    fmt.Println(s1)
}